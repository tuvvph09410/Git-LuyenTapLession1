    public void clear() {
        this.txtMaSach.setText("");
        this.txtTenSach.setText("");
        this.txtSoLuong.setText("");
        this.cbbTinhTrang.setSelectedIndex(0);
    }
    
    public void showTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblSach.getModel();
        
        dtm.setRowCount(0);
        
        List<Sach> listSach = this.qlsi.getList();
        for (int i = 0; i < listSach.size(); i++) {
            Sach sach = listSach.get(i);
            Object[] o = new Object[]{
                sach.getMaSach(),
                sach.getTenSach(),
                sach.getSoLuong(),
                sach.getTinhTrang()
            };
            dtm.addRow(o);
        }
    }
