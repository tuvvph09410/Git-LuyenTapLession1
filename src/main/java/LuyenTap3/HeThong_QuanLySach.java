    private void tblSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSachMouseClicked
        // TODO add your handling code here:
        int row = this.tblSach.getSelectedRow();
        if (row == -1) {
            return;
        }
        
        String maSach = this.tblSach.getValueAt(row, 0).toString();
        String tensach = this.tblSach.getValueAt(row, 1).toString();
        String soLuong = this.tblSach.getValueAt(row, 2).toString();
        String tinhTrang = this.tblSach.getValueAt(row, 3).toString();
        
        this.txtMaSach.setText(maSach);
        this.txtTenSach.setText(tensach);
        this.txtSoLuong.setText(soLuong);
        this.cbbTinhTrang.setSelectedItem(tinhTrang);
    }//GEN-LAST:event_tblSachMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = this.tblSach.getSelectedRow();
        if (row == -1) {
            return;
        }
        String maSach = this.txtMaSach.getText();
        String tenSach = this.txtTenSach.getText();
        String soLuong = this.txtSoLuong.getText();
        String tinhTrang = this.cbbTinhTrang.getSelectedItem().toString();
        
        if (maSach.length() != 0 && tenSach.length() != 0 && soLuong.length() != 0) {
            try {
                int soLuongInt = Integer.parseInt(soLuong);
                this.txtSoLuong.setBackground(Color.white);
                Sach sach = new Sach(maSach, tenSach, soLuongInt, tinhTrang);
                this.qlsi.update(row, sach);
                this.clear();
                this.showTable();
                JOptionPane.showMessageDialog(this, "Sửa thành công!");
            } catch (NumberFormatException e) {
                e.printStackTrace();
                this.txtSoLuong.setBackground(Color.yellow);
                JOptionPane.showMessageDialog(this, "Nhập sai định dạng, vui lòng nhập lại!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa nhập đầy đủ, vui lòng nhập đầy đủ!");
        }

    }//GEN-LAST:event_btnSuaActionPerformed

