    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        int row = this.tblSanPham.getSelectedRow();
        if (row == -1) {
            return;
        }

        String name = this.tblSanPham.getValueAt(row, 0).toString();
        String ma = this.tblSanPham.getValueAt(row, 1).toString();
        String danhMuc = this.tblSanPham.getValueAt(row, 2).toString();
        String tinhTrang = this.tblSanPham.getValueAt(row, 3).toString();
        String donGia = this.tblSanPham.getValueAt(row, 4).toString();

        this.txtName.setText(name);
        this.txtMa.setText(ma);
        this.cbbDanhMuc.setSelectedItem(danhMuc);
        if (tinhTrang.equals("Còn hàng")) {
            this.radioConHang.setSelected(true);
        } else {
            this.radioHetHang.setSelected(true);
        }
        this.txtGia.setText(donGia);
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = this.tblSanPham.getSelectedRow();
        if (row == -1) {
            return;
        }
        String name = this.txtName.getText();
        String ma = this.txtMa.getText();
        String danhMuc = this.cbbDanhMuc.getSelectedItem().toString();
        String tinhTrang = "";
        if (this.radioConHang.isSelected() == true) {
            tinhTrang = "Còn hàng";
        } else {
            tinhTrang = "hết hàng";
        }
        String gia = this.txtGia.getText();
        if (name.length() != 0 && ma.length() != 0 && gia.length() != 0) {
            try {
                Double giaDouble = Double.parseDouble(gia);
                Product product = new Product(name, ma, danhMuc, tinhTrang, giaDouble);
                this.pmi.update(row, product);
                this.showTable();
                this.txtGia.setBackground(Color.white);
                this.reSet();
                JOptionPane.showMessageDialog(this, "Sửa thành công!");
            } catch (NumberFormatException e) {
                e.printStackTrace();
                this.txtGia.setBackground(Color.yellow);
                JOptionPane.showMessageDialog(this, "Đã nhập sai định dạng giá, vui lòng nhập lại đơn giá");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin !");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

