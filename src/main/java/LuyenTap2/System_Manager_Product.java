    public void reSet() {
        this.txtName.setText("");
        this.txtGia.setText("");
        this.txtMa.setText("");
        this.cbbDanhMuc.setSelectedIndex(0);
        this.radioConHang.setSelected(true);
    }

    public void showTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblSanPham.getModel();

        dtm.setRowCount(0);

        List<Product> listProduct = this.pmi.getList();
        for (int i = 0; i < listProduct.size(); i++) {
            Product product = listProduct.get(i);
            Object[] objects = new Object[]{
                product.getNameSP(),
                product.getMaSP(),
                product.getDanhMuc(),
                product.getTrangThai(),
                product.getDonGia()
            };
            dtm.addRow(objects);
        }
    }

